describe("formatData()", () => {
  it("if the array is empty it returns an empty object", () => {
    const actual = formatData([]);
    const expected = [];
    expect(actual).toEqual(expected);
  });
  it("returns object with key changed for a single object array", () => {
    const arrOfObj = [
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        belongs_to: "Living in the shadow of a great man",
        created_by: "butter_bridge",
        votes: 16,
        created_at: 1511354163389,
      },
    ];
    const refObj = { "Living in the shadow of a great man": 1 };
    const actual = formatData(arrOfObj, refObj, "belongs_to", "article_id");
    const expected = [{
      body:
        "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
      article_id: 1,
      created_by: "butter_bridge",
      votes: 16,
      created_at: 1511354163389,
    }];
    expect(actual).toEqual(expected);
  });
  it("returns object with key changed for a many object array", () => {
    const arrOfObj = [
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        belongs_to: "They're not exactly dogs, are they?",
        created_by: 'butter_bridge',
        votes: 16,
        created_at: 1511354163389,
      },
      {
        body:
          'The beautiful thing about treasure is that it exists. Got to find out what kind of sheets these are; not cotton, not rayon, silky.',
        belongs_to: 'Living in the shadow of a great man',
        created_by: 'butter_bridge',
        votes: 14,
        created_at: 1479818163389,
      }
    ];
    const refObj = { "Living in the shadow of a great man": 1, "They're not exactly dogs, are they?": 2};
    const actual = formatData(arrOfObj, refObj, "belongs_to", "article_id");
    const expected = [
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        article_id: 2,
        created_by: 'butter_bridge',
        votes: 16,
        created_at: 1511354163389,
      },
      {
        body:
          'The beautiful thing about treasure is that it exists. Got to find out what kind of sheets these are; not cotton, not rayon, silky.',
        article_id: 1,
        created_by: 'butter_bridge',
        votes: 14,
        created_at: 1479818163389,
      }
    ];
    expect(actual).toEqual(expected);
  });
  it("doesnt mutate the array of objects passed in", () => {
    const arrOfObj = [
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        belongs_to: "They're not exactly dogs, are they?",
        created_by: 'butter_bridge',
        votes: 16,
        created_at: 1511354163389,
      },
      {
        body:
          'The beautiful thing about treasure is that it exists. Got to find out what kind of sheets these are; not cotton, not rayon, silky.',
        belongs_to: 'Living in the shadow of a great man',
        created_by: 'butter_bridge',
        votes: 14,
        created_at: 1479818163389,
      }
    ];
    const refObj = { "Living in the shadow of a great man": 1, "They're not exactly dogs, are they?": 2};
    formatData(arrOfObj, refObj, "belongs_to", "article_id");
    expect(arrOfObj).toEqual([
      {
        body:
          "Oh, I've got compassion running out of my nose, pal! I'm the Sultan of Sentiment!",
        belongs_to: "They're not exactly dogs, are they?",
        created_by: 'butter_bridge',
        votes: 16,
        created_at: 1511354163389,
      },
      {
        body:
          'The beautiful thing about treasure is that it exists. Got to find out what kind of sheets these are; not cotton, not rayon, silky.',
        belongs_to: 'Living in the shadow of a great man',
        created_by: 'butter_bridge',
        votes: 14,
        created_at: 1479818163389,
      }
    ]);
  });
});
